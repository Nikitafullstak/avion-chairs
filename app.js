function tabl(){
    let elem = document.getElementById('imh3');
    let style = getComputedStyle(elem);
    if (style.display === 'none'){
        document.getElementById('imh3').style.display='block';

    }else if (style.display !== 'none'){
        document.getElementById('imh3').style.display='none'
    }
}
